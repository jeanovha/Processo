    public void logar() {
        //String md5 = md5(utf8_encode(txtSenha.getText()));

        String sql = "select * from tbusuarios where login=? and senha=?";
//"SELECT *, MD5(senha=?) FROM tbusuarios WHERE login=? MD5(senha=?) LIKE ?";
//"select * from tbusuarios where login=? and senha=?";
        try {
            pst = conexao.prepareStatement(sql);
            pst.setString(1, txtUsuario.getText());
            pst.setString(2, md5(txtSenha.getText()));

            rs = pst.executeQuery();

            if (rs.next()) {

                String perfil = rs.getString(5);
                //System.out.println(perfil);

                if (perfil.equals("admin")) {

                    TelaPrincipal principal = new TelaPrincipal();
                    principal.setVisible(true);

                    TelaPrincipal.menCadUsu.setEnabled(true);
                    TelaPrincipal.lblUsuario.setText(rs.getString(1));
                    TelaPrincipal.lblUsuario.setForeground(Color.red);
                    this.dispose();

                } else {
                    TelaPrincipal principal = new TelaPrincipal();
                    principal.setVisible(true);
                    TelaPrincipal.lblUsuario.setText(rs.getString(1));
                    TelaPrincipal.lblUsuario.setForeground(Color.blue);
                    this.dispose();
                }

                conexao.close();
            } else {
                JOptionPane.showMessageDialog(null, "Usu√°rio ou senha invalido");

            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }

